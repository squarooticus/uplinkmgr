def write_file(path: str, content: str, dry_run: bool = False) -> None:
    if dry_run:
        print(f"--- {path} ---")
        print(content)
        return
    p = Path(path)
    p.parent.mkdir(parents=True, exist_ok=True)
    p.write_text(content)


def write_symlink(link_path: str, target_name: str, dry_run: bool = False) -> None:
    """Create a symlink link_path -> target_name (basename only)."""
    if dry_run:
        print(f"symlink: {link_path} -> {target_name}")
        return
    p = Path(link_path)
    p.parent.mkdir(parents=True, exist_ok=True)
    if p.is_symlink() or p.exists():
        p.unlink()
    p.symlink_to(target_name)


def remove_stale_files(pattern_dir: str, prefix: str, current_names: set[str],
                        suffix: str, dry_run: bool = False) -> None:
    """Remove generated files whose uplink name is no longer in current_names."""
    d = Path(pattern_dir)
    if not d.exists():
        return
    for f in d.iterdir():
        if f.name.startswith(prefix) and f.name.endswith(suffix):
            inner = f.name[len(prefix):-len(suffix)]
            if inner not in current_names:
                if dry_run:
                    print(f"would remove: {f}")
                else:
                    f.unlink()
