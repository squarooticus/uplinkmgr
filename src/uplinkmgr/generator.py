def nat_fragment(cfg: Config) -> str:
    wan_rules = "\n".join(
        f'        oifname "{u.interface}" masquerade'
        for u in cfg.uplinks
    )
    return (
        "# NAT reference fragment generated by uplinkmgr-setup.\n"
        "# This file is NOT applied automatically.\n"
        "# Review and incorporate into your nftables configuration as appropriate.\n"
        "# Regenerate with: uplinkmgr-setup\n"
        "\n"
        "table ip uplinkmgr_nat {\n"
        "    chain postrouting {\n"
        "        type nat hook postrouting priority srcnat; policy accept;\n"
        "\n"
        "        # Masquerade outbound traffic on all WAN interfaces\n"
        f"{wan_rules}\n"
        "    }\n"
        "}\n"
    )


