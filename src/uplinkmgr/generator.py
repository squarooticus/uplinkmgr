INITIAL_VALID_LIFETIME = 7200
INITIAL_PREFERRED_LIFETIME = 1800
INITIAL_DEFAULT_LIFETIME = 1800
INITIAL_ROUTE_LIFETIME = 1800


def radvd_conf(cfg: Config, uplink: UplinkConfig) -> str:
    """Generate initial radvd config. Uses placeholder lifetimes and ::/64 prefixes."""
    lines = [
        f"# Generated by uplinkmgr-setup for uplink: {uplink.name}",
        "# Do not edit by hand. The daemon regenerates this file with live lifetimes.",
        "",
    ]
    for net_idx, net in enumerate(cfg.networks):
        mv = naming.macvlan_name(net.interface, uplink.index)
        lines += _radvd_interface_block(
            iface=mv,
            preference="high",
            default_lifetime=INITIAL_DEFAULT_LIFETIME,
            route_lifetime=INITIAL_ROUTE_LIFETIME,
            prefix="::/64",
            valid_lifetime=INITIAL_VALID_LIFETIME,
            preferred_lifetime=INITIAL_PREFERRED_LIFETIME,
        )
    return "\n".join(lines) + "\n"


def radvd_conf_from_state(
    cfg: Config,
    uplink: UplinkConfig,
    preference: str,
    default_lifetime: int,
    route_lifetime: int,
    per_iface_prefixes: dict[str, str],  # macvlan_name -> prefix/64
    valid_lifetime: int,
    preferred_lifetime: int,
    is_down: bool = False,
) -> str:
    """Generate radvd config with live values from state files."""
    lines = [
        f"# Generated by uplinkmgr daemon for uplink: {uplink.name}",
        "# Do not edit by hand.",
        "",
    ]
    for net in cfg.networks:
        mv = naming.macvlan_name(net.interface, uplink.index)
        prefix = per_iface_prefixes.get(mv, "::/64")
        eff_preferred = 0 if is_down else preferred_lifetime
        eff_valid = 1800 if is_down else valid_lifetime
        lines += _radvd_interface_block(
            iface=mv,
            preference=preference,
            default_lifetime=default_lifetime,
            route_lifetime=route_lifetime,
            prefix=prefix,
            valid_lifetime=eff_valid,
            preferred_lifetime=eff_preferred,
        )
    return "\n".join(lines) + "\n"


def _radvd_interface_block(
    iface: str,
    preference: str,
    default_lifetime: int,
    route_lifetime: int,
    prefix: str,
    valid_lifetime: int,
    preferred_lifetime: int,
) -> list[str]:
    return [
        f"interface {iface}",
        "{",
        "    AdvSendAdvert on;",
        f"    AdvDefaultPreference {preference};",
        f"    AdvDefaultLifetime {default_lifetime};",
        "",
        "    route ::/0",
        "    {",
        f"        AdvRoutePreference {preference};",
        f"        AdvRouteLifetime {route_lifetime};",
        "    };",
        "",
        f"    prefix {prefix}",
        "    {",
        "        AdvOnLink on;",
        "        AdvAutonomous on;",
        "        AdvRouterAddr on;",
        f"        AdvValidLifetime {valid_lifetime};",
        f"        AdvPreferredLifetime {preferred_lifetime};",
        "        DecrementLifetimes on;",
        "    };",
        "",
        "    RDNSS { };",
        "    DNSSL { };",
        "};",
        "",
    ]


