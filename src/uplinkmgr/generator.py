HEADER = "# Generated by uplinkmgr-setup. Do not edit by hand.\n# Regenerate with: uplinkmgr-setup\n"


# ---------------------------------------------------------------------------
# ifupdown
# ---------------------------------------------------------------------------

def interfaces_file(cfg: Config) -> str:
    lines = [HEADER]
    for uplink in cfg.uplinks:
        if not uplink.ipv6_pd:
            continue
        ll = naming.link_local(uplink.index)
        for net_idx, net in enumerate(cfg.networks):
            mv = naming.macvlan_name(net.interface, uplink.index)
            mac = naming.mac_address(uplink.index, net_idx)
            lines.append(f"auto {mv}")
            lines.append(f"iface {mv} inet manual")
            lines.append(f"    pre-up ip link add link {net.interface} name {mv} type macvlan mode bridge")
            lines.append(f"    pre-up ip link set {mv} address {mac}")
            lines.append(f"    pre-up sysctl -q net.ipv6.conf.{mv}.addr_gen_mode=1")
            lines.append(f"    up ip link set {mv} up")
            lines.append(f"    up ip -6 addr add {ll} dev {mv} scope link")
            lines.append(f"    down ip -6 addr del {ll} dev {mv} scope link 2>/dev/null || true")
            lines.append(f"    down ip link del {mv} 2>/dev/null || true")
            lines.append("")
    return "\n".join(lines)


