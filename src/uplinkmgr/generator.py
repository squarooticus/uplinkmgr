def radvd_unit(cfg: Config, uplink: UplinkConfig) -> str:
    name = uplink.name
    conf = naming.radvd_conf_path(name)
    pid = f"/run/radvd-uplinkmgr-{name}.pid"
    dhcpcd_svc = naming.dhcpcd_unit_name(name)
    return (
        f"# Generated by uplinkmgr-setup for uplink: {name}\n"
        f"# Do not edit by hand.\n"
        f"\n"
        f"[Unit]\n"
        f"Description=Router advertisement daemon for uplinkmgr uplink: {name}\n"
        f"After=network.target {dhcpcd_svc}\n"
        f"Requires={dhcpcd_svc}\n"
        f"\n"
        f"[Service]\n"
        f"Type=forking\n"
        f"PIDFile={pid}\n"
        f"ExecStart=/usr/sbin/radvd --configtest --config {conf}\n"
        f"ExecStart=/usr/sbin/radvd \\\n"
        f"    --config {conf} \\\n"
        f"    --pidfile {pid} \\\n"
        f"    --nodaemon\n"
        f"ExecReload=/bin/kill -HUP $MAINPID\n"
        f"Restart=on-failure\n"
        f"RestartSec=5s\n"
        f"\n"
        f"[Install]\n"
        f"WantedBy=multi-user.target\n"
    )


